package toUpperCase_ToLowerCase_method;

public class Demo {
public static void main(String[] args) {
	String s="Hello";
	System.out.println(s);
	System.out.println(s.toUpperCase());
	System.out.println(s.toLowerCase());
	s=s.toUpperCase();
	System.out.println(s);
	s=s.toLowerCase();
	System.out.println(s);
	}
}
